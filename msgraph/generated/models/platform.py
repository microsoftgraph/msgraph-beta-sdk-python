from enum import Enum

class Platform(str, Enum):
    # Unknown device platform
    Unknown = "unknown",
    # IOS device platform
    Ios = "ios",
    # Android device platform
    Android = "android",
    # Windows device platform
    Windows = "windows",
    # WindowsMobile device platform
    WindowsMobile = "windowsMobile",
    # Mac device platform
    MacOS = "macOS",

